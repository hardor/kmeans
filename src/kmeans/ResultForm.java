    public void addRowToTable(int exp, double[][] data, int size,double error) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<String> row = new ArrayList<String>();
        String[] str;
        for (int i = 0; i < size; i++) {
            row.add(Integer.toString(exp));
            row.add(Integer.toString(i + 1));
            str = Arrays.toString(data[i]).split("[\\[\\]]")[1].split(", ");
            row.addAll(Arrays.asList(str));
            row.add(Double.toString(error));
            str = row.toArray(new String[row.size()]);
            model.addRow(str);            
            row.clear();
        }
        model.addRow(new String[row.size()]);

    }

